// Функция sumExcept(a, i, n) принимает три аргумента:
// a — массив чисел (в том числе возможны нечисловые значения),
// i — индекс начала отрезка (может быть некорректным),
// n — количество элементов, начиная с i, которые нужно исключить из суммы (может быть некорректным).

// Функция должна вернуть сумму всех целочисленных значений массива a, кроме тех, что находятся в диапазоне индексов от i до i + n - 1 включительно.

function sumExcept(a, i, n) {
  i = i < 0 || !Number.isInteger(i) ? 0 : i;
  n = n < 0 || !Number.isInteger(n) ? 0 : n;

  if (i >= a.length - 1) {
    return a.reduce((acc, num) => acc + num, 0);
  }

  let result = 0;
  let min = i;
  let max = i + n - 1;

  a.forEach((num, index) => {
    if (Number.isInteger(num) && (index < min || index > max)) {
      result += num;
    }
  });

  return result;
}

// Примеры тестов:
// Массив: [1, 9, 8, 4]
// Индекс начала исключения: 1
// Количество исключаемых элементов: 2 → исключаются элементы с индексами 1 и 2 (то есть 9 и 8)

// Остаются: 1 и 4
// Результат: 1 + 4 = 5
console.log(sumExcept([1, 9, 8, 4], 1, 2) === 5);
